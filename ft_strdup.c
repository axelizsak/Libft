#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*dst;
	
	i = 1;
	while (s1[i - 1])
		i++;
	dst = malloc(sizeof(*s1) * i);
	i = 0;
	if (dst == NULL)
		return (NULL);
	while (s1[i])
	{
		dst[i] = s1[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
